#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	t_byte *ptr;

	ptr = (t_byte *)s;
	while (n)
	{
		if (*ptr == (t_byte)c)
			return (ptr);
		ptr++;
		n--;
	}
	return (0);
}
